public class RowCol {
    private int row;
    private int col;
    public RowCol(int row, int col){
        this.row = row;
        this.col = col;
    }
    public int getRow(){
        return row;
    }

    public int getCol() {
        return col;
    }

    public void setRowCol(int row, int col) {
        this.row = row;
        this.col = col;
    }


}

